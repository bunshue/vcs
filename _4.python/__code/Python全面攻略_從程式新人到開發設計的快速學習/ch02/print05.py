print('%c' %'M')            # 顯示字元「M」
print('%4c' %'M')           # 顯示字元「ΔΔΔM」,靠右對齊,寬度有剩補空格
print('%c' %65)             # 顯示字元「A」,65的ASCII碼為「A」
print('%s' %'ABCDE')        # 顯示字串「ABCDE」
print('%8s' %'ABCDE')       # 顯示字串「ΔΔΔABCDE」
print('%3s' %'ABCDE')       # 顯示字串「ABCDE」,總寬度不足設定無效
print('%6.2s' %'ABCDE')     # 顯示字串「ΔΔΔΔAB」,寬度設為6,顯示2字元


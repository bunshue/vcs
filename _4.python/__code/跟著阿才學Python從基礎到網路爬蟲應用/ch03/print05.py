print('%f'%1234.567)    #小數位數預設6位	1234.567000 
print('%f'%-123.45)     #小數位數預設6位	-123.450000 
print('%.2f'%12.345)    #設小數位數2位,第3位四捨五入	12.35 
print('%8.2f'%-12.3456) #設總寬度8位,小數3位		ΔΔ-12.35
print('%3.1f'%123.45)   #設寬度為3且小數1位，寬度不足時全部顯示	123.5 
print('%8.0f'%-1234.56) #設小數位數0位,第1位四捨五入	ΔΔΔ-1235
print('%8.0f'%1234.56)  #設小數位數0位,第1位四捨五入	ΔΔΔΔ1235 
print('%e'%123.4)       #科學記號小數部分6位,小數位數不足補0 1.234000e+02
print('%10.2e'%12345.6) #設總寬度10,小數2位	ΔΔ1.23e+04 


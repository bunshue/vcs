print('%d'%(12345))     # 顯示整數資料		12345
print('%7d'%(12345))    # 設寬度為7,寬度有剩時補空格	ΔΔ12345 
print('%-7d'%(-12345))  # 靠左對齊,寬度有剩時補空格	-12345Δ
print('%07d'%(12345))   # 設寬度為7,寬度有剩時補0	0012345
print('%4d'%(-12345))   # 設寬度為3,寬度不足時全部顯示-12345 
print('%c'%('Y'))       # 顯示字元「Y」	 Y 
print('%4c'%('Y'))      # 寬度為4有剩補空格	ΔΔΔY
print('%c'%(97))        # 97的ASCII碼為a	 a
print('%s'%('ABCDE'))   # 顯示字串資料	 ABCDE
print('%7s'%('ABCDE'))  # 設寬度為7,寬度有剩時補空格	ΔΔABCDE
print('%4s'%('ABCDE'))  # 設寬度為4,寬度不足時全部顯示 ABCDE
print('%6.3s'%('ABCDE'))# 設寬度為6並只顯示3字元	ΔΔΔABC

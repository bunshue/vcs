a = 60     # 00111100
b = 15     # 00001111
print("a = " + str(a))
print("b = " + str(b))
r = a & b      # AND運算
print("a & b = " + str(r))
c = 3      # 00000011
print("c = " + str(c))
r = a | c      # OR運算子
print("a | c = " + str(r))
d = 120    # 00111100
print("d = " + str(d))
r = a ^ d      # XOR運算子
print("a ^ d = " + str(r))
e = 5      # 00000101
print("e = " + str(e))
r = ~e     # NOT運算子
print("~e = " + str(r))
r = ~r     # NOT運算子
print("~r = " + str(r))
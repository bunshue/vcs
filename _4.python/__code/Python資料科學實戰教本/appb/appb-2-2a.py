str1 = 'welcome to python' 

print("str1 = " + str1)
b = str1.isalnum()
print("str1.isalnum() = " + str(b))
b = str1.isalpha()
print("str1.isalpha() = " + str(b))
b = str1.isdigit()
print("str1.isdigit() = " + str(b))
b = "2016".isdigit()
print('"2016".isdigit() = ' + str(b))
b = str1.isidentifier()
print("str1.isidentifier() = " + str(b))
b = str1.islower()
print("str1.islower() = " + str(b))
b = str1.isupper()
print("str1.isupper() = " + str(b))
b = "   ".isspace()
print('"   ".isspace() = ' + str(b))

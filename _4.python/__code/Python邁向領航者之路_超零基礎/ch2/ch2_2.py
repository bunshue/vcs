hourly_salary = 125                             # 設定時薪
annual_salary = hourly_salary * 8 * 300         # 計算年薪
monthly_fee = 9000                              # 設定每月花費
annual_fee = monthly_fee * 12                   # 計算每年花費
annual_savings = annual_salary - annual_fee     # 計算每年儲存金額
print(annual_savings)                           # 列出每年儲存金額



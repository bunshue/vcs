# Step 1：顯示提示文字"請輸入運算元1: "來輸入整數變數var1的值。
var1 = int(input("請輸入運算元1: "))

# Step 2：顯示提示文字"請輸入運算元2: "來輸入整數變數var2的值。
var2 = int(input("請輸入運算元2: "))

# Step 3：計算2個變數相乘的結果後，儲存至變數var3。
var3 = var1 * var2

# Step 4：輸出文字內容"相乘結果= "和變數var3的值。
print("相乘結果= " + str(var3))

# Step 5：計算2個變數相除的結果後，儲存至變數var3。
var3 = var1 / var2

# Step 6：輸出文字內容"相除結果= "和變數var3的值。
print("相除結果= " + str(var3))

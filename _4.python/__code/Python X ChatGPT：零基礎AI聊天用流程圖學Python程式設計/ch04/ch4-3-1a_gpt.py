# Step 1：提示使用者輸入第一個數字，並將其儲存至變數 var1。
var1 = int(input("請輸入運算元 1: "))

# Step 2：提示使用者輸入第二個數字，並將其儲存至變數 var2。
var2 = int(input("請輸入運算元 2: "))

# Step 3：計算 var1 和 var2 的乘積，並儲存至變數 var3。
var3 = var1 * var2

# Step 4：輸出相乘結果。
print("相乘結果= ", var3)

# Step 5：計算 var1 和 var2 的商，並儲存至變數 var3。
var3 = var1 / var2

# Step 6：輸出相除結果。
print("相除結果= ", var3)

# Step 1：指定整數變數a的值是3。
a = 3
# Step 2：指定整數變數b的值是5。
b = 5
# Step 3：指定整數變數c的值是2。
c = 2

# Step 4：如果變數a大於b，且變數a大於c :
if a > b and a > c:
    # Step 4.1：輸出訊息文字"變數a最大!"。
    print("變數a最大!")
# Step 5：否則，如果變數b大於c :
elif b > c:
    # Step 5.1：輸出訊息文字"變數b最大!"。
    print("變數b最大!")
# Step 5.2：否則 :
else:
    # Step 5.3：輸出訊息文字"變數c最大!"。
    print("變數c最大!")

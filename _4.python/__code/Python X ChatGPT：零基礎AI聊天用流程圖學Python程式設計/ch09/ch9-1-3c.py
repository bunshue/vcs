str1 = 'welcome to python'
print("str1 = ", str1)
b = str1.isalnum()
print("str1.isalnum() = ", b)
b = str1.isalpha()
print("str1.isalpha() = ", b)
b = str1.isdigit()
print("str1.isdigit() = ", b)
b = "2023".isdigit()
print('"2023".isdigit() = ', b)
b = str1.isidentifier()
print("str1.isidentifier() = ", b)
b = str1.islower()
print("str1.islower() = ", b)
b = str1.isupper()
print("str1.isupper() = ", b)
b = "   ".isspace()
print('"   ".isspace() = ', b)

"""
等差數列 等比數列
等差級數 等比級數

"""

print('------------------------------------------------------------')	#60個

#等差數列 等差級數 等比數列 等比級數
for i in range(10):
    print(i)

x = range(11) # 0, 1, 2, ... 10
s = sum(x)
print('等差級數 :', s)

x = range(3, 21, 3) # 3, 6, 9, ... 18
for i in x:
    print(i)
s = sum(x)
print('等差級數 :', s)

print('------------------------------------------------------------')	#60個


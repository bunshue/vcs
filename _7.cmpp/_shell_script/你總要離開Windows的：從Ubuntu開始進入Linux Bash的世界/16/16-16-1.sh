#! /bin/bash

if [  $choose -eq 1 ]
then
    echo $num1 + $num2 = $((num1+num2))
elif [  $choose -eq 2 ]
then
    echo $num1 - $num2 = $((num1-num2))
elif [  $choose -eq 3 ]
then
    echo $num1 * $num2 = $((num1*num2))
elif [  $choose -eq 4 ]
then
    echo $num1 / $num2 = $((num1/num2))
else
then
    echo """"
fi
